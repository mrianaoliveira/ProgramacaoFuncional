import java.util.ArrayList;

public class Final {
    public static void main(String[] args) {

        ArrayList<Int> um = new ArrayList<>();
        ArrayList<Int> dois = new ArrayList<>();

        for(Int u : um) {
            for ( Int d : dois) {
                if(um[u] == dois[d]) {
                    System.out.println("[" + um);
                }
            }
        }
        System.out.println("]");
    }
}